def translate_text(text):
    """중국어를 건축 전문 한국어로 번역"""
    result = text
    # 긴 단어부터 치환 (부분 매칭 방지)
    sorted_terms = sorted(TERM_MAP.items(), key=lambda x: len(x[0]), reverse=True)
    for cn, kr in sorted_terms:
        result = result.replace(cn, kr)
    return result


def classify_by_trade(text):
    """텍스트에서 건축 공종 분류"""
    trades = []
    for trade_name, info in TRADE_SYSTEM.items():
        for kw in info['keywords']:
            if kw in text:
                trades.append(trade_name)
                break
    return trades


def extract_worker_count(messages_text):
    """대화에서 작업인원 수 추출"""
    count = 0
    patterns = [
        r'(\d+)\s*[人名个]\s*(?:施工|작업|투입)',
        r'(?:투입|작업|시공)\s*(?:인원|인력)?\s*[:：]?\s*(\d+)',
        r'(\d+)\s*명\s*(?:투입|작업|시공)',
        r'人员[：:]\s*(\d+)',
        r'작업인원\s*[:：]?\s*(\d+)',
        r'投入\s*(\d+)\s*人',
        r'(\d+)\s*人\s*施工',
    ]
    for pat in patterns:
        m = re.search(pat, messages_text)
        if m:
            count = max(count, int(m.group(1)))
    return count if count > 0 else None


def extract_work_details_by_trade(messages, date_str):
    """공종별 작업 상세내용 추출"""
    all_text = ' '.join([m.get('text', '') for m in messages])
    trade_details = {}

    for trade_name, info in TRADE_SYSTEM.items():
        is_relevant = any(kw in all_text for kw in info['keywords'])
        if not is_relevant:
            continue

        # 해당 공종 관련 메시지만 필터링
        relevant_msgs = []
        for m in messages:
            text = m.get('text', '')
            if any(kw in text for kw in info['keywords']):
                translated = translate_text(text[:200])
                relevant_msgs.append({
                    'time': m.get('time', ''),
                    'content': translated
                })

        if relevant_msgs:
            trade_details[trade_name] = {
                'color': info['color'],
                'messages': relevant_msgs[:5],  # 최대 5건
                'count': len(relevant_msgs),
            }

    return trade_details


def process_raw_messages():
    """lark_raw_messages.json에서 날짜별 텍스트 메시지 그룹화"""
    if not os.path.exists(RAW_MSG_FILE):
        return {}

    with open(RAW_MSG_FILE, encoding='utf-8') as f:
        raw = json.load(f)

    date_groups = defaultdict(list)
    for m in raw:
        if m.get('msg_type') not in ('text', 'post'):
            continue
        ts = int(m.get('create_time', '0'))
        if ts > 1e12:
            ts /= 1000
        try:
            dt = datetime.fromtimestamp(ts, tz=KST)
        except:
            continue

        content_str = m.get('body', {}).get('content', '{}')
        if m.get('msg_type') == 'text':
            try:
                text = json.loads(content_str).get('text', '')
            except:
                text = content_str
        elif m.get('msg_type') == 'post':
            try:
                content = json.loads(content_str)
                texts = []
                title = content.get('title', '')
                if title:
                    texts.append(title)
                for lang, paragraphs in content.items():
                    if isinstance(paragraphs, list):
                        for para in paragraphs:
                            if isinstance(para, list):
                                for elem in para:
                                    if isinstance(elem, dict) and elem.get('tag') == 'text':
                                        texts.append(elem.get('text', ''))
                text = ' '.join(texts)
            except:
                text = ''
        else:
            text = ''

        if text.strip() and len(text.strip()) > 2:
            date_groups[dt.strftime('%Y-%m-%d')].append({
                'time': dt.strftime('%H:%M'),
                'text': text.strip()
            })

    return dict(date_groups)


def main():
    print('=' * 55)
    print('  🔄 작업일지 데이터 고도화')
    print('=' * 55)

    # 1. 기존 데이터 로드
    with open(DATA_FILE, encoding='utf-8') as f:
        data = json.load(f)

    # 2. Raw 메시지 날짜별 그룹
    print('📨 Raw 메시지 분석 중...')
    date_msgs = process_raw_messages()
    print(f'  {len(date_msgs)}일 분량 메시지 처리')

    # 3. 각 일지 고도화
    for log in data['logs']:
        date = log['log_date']
        msgs = date_msgs.get(date, [])
        all_text = ' '.join([m['text'] for m in msgs])

        # 3-1. 공종별 분류 (건축 표준)
        existing_trades = log.get('trades')
        if log.get('pdf_parsed') or (isinstance(existing_trades, dict) and existing_trades):
            trades = list(existing_trades.keys()) if isinstance(existing_trades, dict) else existing_trades
        else:
            trades = classify_by_trade(all_text)
            log['trades'] = trades

        # 3-2. 작업인원수 추출
        if log.get('pdf_parsed') and log.get('total_workers'):
            worker_count = log.get('total_workers')
            log['worker_count'] = worker_count
        else:
            worker_count = extract_worker_count(all_text)
            log['worker_count'] = worker_count

        # 3-3. 공종별 작업 상세내용
        trade_details = extract_work_details_by_trade(msgs, date)
        log['trade_details'] = trade_details

        # 3-4. 대화 내용 한국어 번역
        translated_highlights = []
        for h in log.get('day_highlights', []):
            translated_highlights.append(translate_text(h))
        log['day_highlights_kr'] = translated_highlights

        # 3-5. 기존 work_categories도 번역
        log['work_categories_kr'] = log.get('work_categories', [])

        if trades:
            print(f'  ✅ {date}: {", ".join(trades[:4])}{"..." if len(trades)>4 else ""}'
                  + (f' | 인원: {worker_count}명' if worker_count else ''))

    # 4. 공종 색상 매핑 저장
    data['trade_system'] = {k: {'color': v['color']} for k, v in TRADE_SYSTEM.items()}
    data['last_enhanced'] = datetime.now(KST).strftime('%Y-%m-%d %H:%M:%S KST')

    # 5. 저장
    with open(OUTPUT_FILE, 'w', encoding='utf-8') as f:
        json.dump(data, f, ensure_ascii=False, indent=2)

    print(f'\n💾 {OUTPUT_FILE} 저장 완료!')
    print(f'  총 {len(data["logs"])}개 일지 고도화')


if __name__ == '__main__':
    main()
